    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(null,"Do you want to clear graph and table?", "Clear Stuffs?",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE);
        
        if(result == JOptionPane.YES_OPTION){
            btnSet.setEnabled(true);
            btnClrSet.setEnabled(true);
            btnCalculate.setEnabled(true);
            
            tf_K0.setEditable(true);
            tf_N.setEditable(true);
        
            //clear graph
            ((Graph)GraphPanel).clearGraph();
            //clear table
            DefaultTableModel model = (DefaultTableModel) tableDisplayK.getModel();
            model.setRowCount(0);
            
            btnClear.setEnabled(false);
        }
    }//GEN-LAST:event_btnClearActionPerformed

