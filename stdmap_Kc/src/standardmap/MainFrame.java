    private void btnSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetActionPerformed
        // TODO add your handling code here:
        
        isSet = !tf_K0.getText().isEmpty() && !tf_N.getText().isEmpty();
        try{
            if(isSet){
                K = Double.parseDouble(tf_K0.getText()); 
                N = Integer.parseInt(tf_N.getText());
                
                if(N>0 && K>=0){
                    tf_K0.setEditable(false); tf_N.setEditable(false);
                    btnSet.setEnabled(false);
                }else if(N<=0){
                    JOptionPane.showMessageDialog(null,"N must be positive","Positive N",JOptionPane.WARNING_MESSAGE);
                }else if(K<0){
                    JOptionPane.showMessageDialog(null,"K must be positive or zero","Not Negative K",JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null,"Please Set both values.","Set",JOptionPane.WARNING_MESSAGE);
            }
            
        }catch(java.lang.NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please input positive number.","Be Positive",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSetActionPerformed

